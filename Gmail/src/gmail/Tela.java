    private void jTextFieldParaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldParaFocusLost
        try {
            String str = jTextFieldPara.getText();
            str = str.replace(',', '#');
            String para[] = str.split("#");
            String semEspaco = "";
            for (int i = 0; i < para.length; i++) {
                char a[] = para[i].toCharArray();
                semEspaco = "";
                for (int j = 0; j < a.length; j++) {
                    if (a[j] != ' ') {
                        semEspaco += a[j];
                    }
                }
                para[i] = semEspaco;
            }
            if (para.length > 0) {
                email.setSendTo(para);
            } else {
                jTextFieldPara.setText("ERRO");
            }
        } catch (Exception e) {
            jTextFieldPara.setText("ERRO");
        }

    }//GEN-LAST:event_jTextFieldParaFocusLost

