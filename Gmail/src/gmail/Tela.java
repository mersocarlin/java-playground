    private void jButtonEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnviarActionPerformed
        if ((jTextFieldDe.getText().equals("")) || (jTextFieldPara.getText().equals("")) || (jTextFieldTitulo.getText().equals(""))) {
            JOptionPane.showMessageDialog(null, "E-mail nao pode ser enviado!");
        } else {
            email.setEmailMsgTxt(jTextAreaMensagem.getText());
            Security.addProvider(new com.sun.net.ssl.internal.ssl.Provider());
            try {
                email.sendSSLMessage(email.getSendTo(), email.getEmailSubjectTxt(), email.getEmailMsgTxt(), email.getEmailFromAddress());
            } catch (MessagingException ex) {
                System.out.println("Erro!");
            }
            System.out.println("Sucessfully Sent mail to All Users");
            JOptionPane.showMessageDialog(null, "E-mail enviado!");
        }
    }//GEN-LAST:event_jButtonEnviarActionPerformed

