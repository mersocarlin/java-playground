    private void jMenuItemSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemSalvarActionPerformed
        try {
            JOptionPane.showMessageDialog(null, dao.incluir(this.pegaDados()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro na insercao!");
        }
        this.limpa();
    }//GEN-LAST:event_jMenuItemSalvarActionPerformed

    private void jMenuItem4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem4ActionPerformed
        try {
            JOptionPane.showMessageDialog(null, dao.atualizar(Integer.parseInt(jTextFieldID.getText()), this.pegaDados()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro na atualizacao!");
        }
        this.limpa();
    }//GEN-LAST:event_jMenuItem4ActionPerformed

    private void jMenuItem5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem5ActionPerformed
        int resposta = 0;
        try {
            resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente apagar?", "Exclusao",JOptionPane.YES_NO_OPTION);
            if(resposta == 0){
                JOptionPane.showMessageDialog(null, dao.excluir(Integer.parseInt(jTextFieldID.getText())));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro na exclusao");
        }
        this.limpa();
    }//GEN-LAST:event_jMenuItem5ActionPerformed

