    private void logaSistema() {
        String login = JOptionPane.showInputDialog(null, "Login", "Digite seu login");
        if (!login.equals("")) {
            email.setLogin(login);
            email.setEmailFromAddress(login);
            jTextFieldDe.setText(login);
            String senha = JOptionPane.showInputDialog(null, "Senha", "Digite sua senha");
            if (!senha.equals("")) {
                email.setSenha(senha);
            } else {
                System.exit(0);
            }
        } else {
            System.exit(0);
        }
    }
