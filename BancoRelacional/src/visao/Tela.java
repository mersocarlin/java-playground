    private Pessoa pegaDados() {
        pessoa = new Pessoa();
        try {
            pessoa.setId(Integer.parseInt(jTextFieldID.getText()));
            pessoa.setNome(jTextFieldNome.getText());
            pessoa.setTelefone(jTextFieldTelefone.getText());
        } catch (Exception e) {
            return null;
        }
        return pessoa;
    }

    private void limpa(){
        jTextFieldID.setText(""+dao.maiorID());
        jTextFieldNome.setText("");
        jTextFieldTelefone.setText("");
        jTextFieldNome.requestFocus();
    }

    private void mostraDados(Pessoa pessoa){
        jTextFieldID.setText(""+pessoa.getId());
        jTextFieldNome.setText(pessoa.getNome());
        jTextFieldTelefone.setText(pessoa.getTelefone());
    }
